            <h2>Introduction</h2>
            <p>All students and staff of the College are entitled to register as members of the Learning Resources and to use the facilities of the Learning Resources for the purpose of their course of study or College employment.</p>
            <br>
            <p>Membership may also be extended to other individuals and to persons nominated from other organisations:  a fee is usually charged.  Access to the Learning Resources for study purposes may be made available to non-members by arrangement. Details are available in the <i>External Services</i> leaflet.</p>
            <br>
            <p>Use of Learning Resources facilities implies acceptance of the Learning Resources rules printed below.  These are designed to protect the interests of all users of Learning Resources and to ensure that the College operates within the law and can carry out its functions efficiently and effectively.  The Learning Resources rules apply in principle to all Learning Resources sites, including workstation rooms and site libraries, not withstanding some variations in practice in the site libraries.  Admission to the Learning Resources is conditional upon observation of the rules and the staff of the Learning Resources are empowered to enforce them.</p>
            <br>
            <p>Breaches of the rules may be dealt with by specific Learning Resources Disciplinary Action (see below), including the withdrawal of some or all Learning Resources facilities, and if appropriate by invoking the College's <i>Regulations for Students,</i> in the case of students, or in the case of staff through the appropriate College procedures as set out in the <i>Staff Handbook.</i></p>
            <br>
            <h2>Rules</h2>
            <h3>A: Access</h3>
            <ul>
                <p><b>I.</b>	The use of Learning Resources facilities is normally limited to use for the educational and other purposes of the College.</p>
                <br>
                <p><b>II.</b>	All individuals wishing to become members of the Learning Resources must comply with the registration procedures.  These include an undertaking to abide by Learning Resources rules.</p>
                <br>
                <p><b>III.</b>	Individuals or organisations who are not eligible for membership but have good reason to use the Learning Resources  may be admitted, by arrangement, for purposes of study only. Procedures for seeking admission are outlined in the External Services leaflet. Non-members must sign the Visitors' Book on each visit, and may be required to produce written identification.  Study use does not include access to the College's Academic Network.</p>
                <br>
                <p><b>IV.</b>	ID cards issued by the College or Learning Resources are not transferable from one person to another.  The person named on each card is responsible for all items borrowed on it and is liable for the cost of replacement of the card or items borrowed on it in the event of loss or damage.</p>
            </ul>
            <br>
            <h3>Use of Facilities</h3>
            <ul>
                <p><b>I.</b>	The Learning Resources is not responsible for loss of, or damage to, the personal belongings of its users and visitors.  Users must take reasonable precautions against theft of their belongings, and must not leave belongings unattended even for a very short period. Study tables/PCs may not be reserved by depositing clothing or private property on the adjacent seat.  Articles left unattended may be removed by Learning Resources staff but will be returned on request at the Reception Desk.</p>
                <br>
                <p><b>II.</b>	All users are required to conduct themselves quietly throughout the Learning Resources and conversation between users must be kept to a minimum.  No conversation is permitted in those parts of the Learning Resources designated as ‘Silent’ areas.  </p>
                <br>
                <p><b>III.</b>	Personal stereos may not be used in the Learning Resources.</p>
                <br>
                <p><b>IV.</b>	Mobile phones must be turned off in all Learning Resources study areas. Their use is permitted in the enclosed stair wells of the Main Site building.  Circumstances at the Clinical Site Libraries may vary.</p>
                <br>
                <p><b>V.</b>	Users should not make use of mains electrical supplies to power their own equipment, including external storage devices,  unless the equipment has been tested and confirmed as meeting the required Health & Safety standards.  Written evidence of such compliance will be required.</p>
                <br>
                <p><b>VI.</b>	Eating and drinking are not permitted in any of the public areas of the Learning Resources.</p>
                <br>
                <p><b>VII.</b>	Action will be taken against any person misusing furniture, facilities or materials.  Any such person may be required to pay for the making good damage or loss.</p>
            </ul>
